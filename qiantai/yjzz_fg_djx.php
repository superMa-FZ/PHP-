<span>一、位置<br/><br/>

        吕萨吕斯酒堡（（Chateau d'Yquem of Lvsa-Lvsi，也被译为伊甘酒庄)、滴金庄园）位于波尔多市西南30公里处一座海拔250米的坡顶上。<br/><br/>

二、荣誉<br/><br/>

        在著名的1855年波尔多官方评级中，吕萨吕斯酒堡（Chateau d'Yquem of Lvsa-Lvsi）被定为唯一的超一级酒庄(Premier Cru Superieur)，凌驾于现今的包括拉菲、拉图、玛歌在内的5大酒庄之上。<br/><br/>

三、质量<br/><br/>

        吕萨吕斯酒堡（Chateau d’Yquem of Lvsa-Lvsi）的葡萄收割和采摘一般都是手工完成的，葡萄不仅要经过连续的筛选，更要一颗一颗地单独严选，来确保摘下来的每一颗都是彻底成熟的。滴金庄园的葡萄筛选过程是出了名的严苛的，一棵葡萄藤上的葡萄只能酿造出一杯葡萄酒，有时候，如果吕萨吕斯酒堡（Chateau d’Yquem of Lvsa-Lvsi）认为酒的质量不够格，整年都可能不出产任何一瓶酒，这种情况在1972年、1974年和1992年都曾发生过。<br/><br/>


四、口感<br/><br/>

        饱满、平衡、微微的黄油和橙类水果味、回味复杂且悠长、醇厚而甜蜜，些许糖分挂杯，深厚绵长的酒香、陈年能力不可估量<br/><br/>

五、改变<br/><br/>

        2004年5月，吕萨吕斯（Lvsa-Lvsi）家族正式将管理权交给LVMH集团。标志着由家族拥有转为家族与大型集团共同拥有。</span><br/><br/>
