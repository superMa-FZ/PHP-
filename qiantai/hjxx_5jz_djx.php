<h4>1.拉菲酒庄</h4>
<div><img src="../photo/lafei.jpg"></div><br/>
<span>拉斐酒庄是1855年波尔多葡萄酒评级时的顶级葡萄酒庄之一。连同奥比安酒庄、拉图酒庄、玛歌酒庄及1973年入选的茂同酒庄，并称波尔多“五大”名庄。拉斐酒庄被认为是“五大”中最典雅的。
拉斐酒庄历史悠久，已有数百年历史。自17世纪西格家族入主后，酒品得到大幅提升。老西格去世后，其次子亚力山大与波尔多另一名庄拉图酒庄的女继承人结婚，此举使他们的儿子小亚力山大成为掌控“五大”中两大名庄的“葡萄王子”Prince des Vignes。
对法国葡萄酒痴迷有加的美国前总统托马斯－杰弗逊也对拉斐酒庄评价甚高。拉斐酒庄的红酒，通常要在不锈钢发酵罐中放3个星期，再在新橡木桶中放18－24个月。酒庄的正牌酒单宁丰厚，可历久藏。
拉斐堡正牌红酒罗斯柴尔德必须等到至少10年左右，真正的面貌才会呈现出来，芳香、水果香，还夹杂着很多丰富的味觉。拉斐堡罗斯柴尔德在质与量上均可称为“酒国巨人”，1996年10月2日在巴黎铁塔所举行的一场筹措装修美法友谊馆经费的拍卖会上，一瓶1864年的拉斐堡罗斯柴尔德拍出5万多法郎的高价。充分证明了名酒如名画一样，都是可以永久收藏的艺术品。拉斐堡罗斯柴尔德的大名，又一次扬名全世界。</span>
<h4>2.玛歌酒庄</h4>
<div><img src="../photo/mage.jpg"></div><br/>
<span>玛歌红酒有力却细致，文雅的果香和木香均衡地搭配在一起，优雅怡人，正如它的女庄主所说的那样：“如同带了天鹅绒手套的铁拳，柔中到刚……”
玛歌酒庄位于波尔多著名的红酒产区梅多克，是法国，也是世界上最负盛名的顶级酒庄之一。在1855年的评级中，玛歌酒庄与拉斐堡、拉图堡和奥比安一起被评为四大红酒一等酒庄。在1973年的重新评定中，玛歌酒庄以其完美的品质再次享有殊荣，在这次评定中得以一等酒庄。
玛歌红酒颜色优美，气味香甜优雅，酒体结构紧密细致，入口温柔典雅，而且平易近人。你会感觉到它味道浓烈却不上头，喝起来口感浓香而不易醉。玛歌酒庄的正牌红酒是一种适合心平气和地品尝和体会的酒，有人形容玛歌红酒像优美婉转的歌声，余音袅袅令人陶醉不已。
波尔多五大顶级酒庄中，玛歌酒庄的独特之处如同酒庄的建筑一样，是一种优雅迷人与气势磅礴的完美结合。</span>
<h4>3.木桐堡庄园</h4>
<div><img src="../photo/mutong.jpg"></div><br/>
<span>木桐堡红酒历史上最奇异的波尔多红酒木桐堡以为戈雅，塞尚和毕加索这些伟大的艺术家而成为艺术的宫殿；木桐堡红酒更因为超绝的品质而成为真正能流芳百世的葡萄酒。正是这种艺术与品质的结合，成就了历史上最奇异的波尔多红酒。把艺术与葡萄酒相结合最成功的酒园首推木桐堡，木桐堡庄园是法国葡萄酒庄园仅有的五家一级酒庄之一。该酒庄所产的葡萄酒一直是葡萄酒收藏者的不二选择，特别是它的标签，从1945年至今，木桐堡酒庄每年都会邀请一位著名的艺术家为该年份的酒瓶标签做设计，因此每年份的酒瓶都是一件艺术品。
木桐罗斯柴尔德酒庄是波尔多最吸引人的地方之一，来自世界各地的葡萄酒和艺术爱好者视这儿为心中的圣地。在这里不仅能买到当年路易十四喜爱的顶级好酒，还能参观酒庄中著名的藏画和珍贵的酒标原作。</span>
<h4>4.拉图酒庄</h4>
<div><img src="../photo/latu.jpg"></div><br/>
<span>拉图酒庄是1855年波尔多葡萄酒评级时的顶级葡萄酒庄之一。连同奥比安酒庄、玛歌酒庄、拉斐酒庄及1973年入选的茂同酒庄，并称波尔多“五大”名庄，风格在“五大”中最为刚劲浑厚。</span>
<h4>5.奥比昂酒庄</h4>
<div><img src="../photo/aobi.jpg"></div><br/>
<span>奥比昂酒庄位于波尔多居民区中心，从17世纪起就以红葡萄酒闻名，而它的白葡萄酒也是难得的绝妙。
奥比昂酒庄位于波尔多吉伦特河左岸的佩萨克（PessacLeognan）地区。是所有酒庄中离波尔多最近的一个。历史上从来都是葡萄成熟最早，以第一个采摘葡萄而标志着波尔多葡萄收获季节的开始。在波尔多“5大酒庄”中最小，却最早成名。考古资料显示，奥比昂酒庄这一带早在罗马时代就已开始栽种葡萄，然而关于葡萄园最早的正式记载则出现在1423年以后。
奥比昂葡萄酒曾经受到很多王室的青睐，被称为“明星酒”。为使奥比昂酒无愧于“明星”这一称号，酿酒师在酒的用料配比中注意了品丽珠与美乐的平衡，这些酒静静地在酒窖里陈化，以确保酒香独具个性。只有在奥比昂这块土地上产出的葡萄酿制的酒才具有这种个性。</span><br/><br/>
