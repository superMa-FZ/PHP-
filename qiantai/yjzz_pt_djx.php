<span id="pt">葡萄酒与春天的约会！</span>
<p id="pp">更新时间：2018-02-05 16:10:22</p><hr/><br/>
<span>春天来临，草长莺飞、百花盛开，正是约上三五好友走出室内，走进大自然的大好时光。郊游踏青、野餐野炊不失为亲近大自然的最好方式。靓丽的春装、鲜艳的妆容、美味的食物、充足的饮料，都是郊游不可或缺的装备。不过，千万不要忘记带上瓶美味可口的佳酿同行，否则，古人诗中“青旗沽酒趁梨花”里的逍遥自在又怎么体会呢？下面就请跟随小醍一同来看看，如何带着葡萄酒去尽情地与春天约会。</span><br/>
<h4>最适宜：甜型白葡萄酒</h4>
<div><img src="../photo/pt1.jpg"></div><br/>
<span>以葡萄酒为主题的漫画《神之水滴》的作者曾说过：“百花盛开之时，正是喝白葡萄酒的好时光。”的确，白葡萄酒的颜色多呈现禾杆黄色、金黄色或果绿色，香味也多以花香、果香为主，而甜型的白葡萄酒容易使人联想到春天温暖的阳光，在百花盛开、阳光和煦的春天里饮用可谓再合适不过。</span>
<h4>最欢快：起泡葡萄酒</h4>
<div><img src="../photo/pt2.jpg"></div><br/>
<span>郊游是让人倍感愉悦欢畅的事情，这时候怎么少得了被称为“欢快之酒”的起泡葡萄酒呢？起泡葡萄酒从诞生之日起，就代表着欢快与愉悦，象征着浪漫和嗨皮，也是唯一可以用于尽情干杯的葡萄酒。为郊游选择一瓶甜度较低酸度适宜的干型起泡酒，不仅能成功地“挑逗”起驴友们的味蕾，还可以一扫宅太久后发霉的心情。</span>
<h4>最经典：干红</h4>
<div><img src="../photo/pt3.jpg"></div><br/>
<span>干红作为葡萄酒中最经典的酒种，以其艳丽的色彩、浓郁的香味和醇厚的口感轻松“征服”了各国美酒爱好者的味蕾，不管是在正式的酒局场合，还是在随意轻松的郊游场合，干红永远都是最经典的选择。</span>
<h4>最温馨：桃红葡萄酒</h4>
<div><img src="../photo/pt4.jpg"></div><br/>
<span>如果郊游活动里有你心仪的她参与，那带上瓶桃红葡萄酒就很温馨也很百搭。桃红葡萄酒的色泽虽然没有干红那般明艳，但她独有的玫瑰红、粉红等色泽更适合桃红柳绿的春季，其香气也多以浓厚柔和的水果香为主，所含丹宁较少，口感也不像干红葡萄酒那样涩口。正是这些特性，才让桃红葡萄酒更平易近人。甜型的桃红葡萄酒配上美味的糕点，很容易受到女士的喜爱。</span><br/><br/>
